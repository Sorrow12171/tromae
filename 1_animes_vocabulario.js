// Función para obtener vocabulario de animes
function obtenerVocabularioAnime(contenedor, subcontenedor, mazo) {
    const key = `sub${contenedor}_${subcontenedor}`;
    if (animesVocabularioDatabase[key] && animesVocabularioDatabase[key][mazo]) {
        return animesVocabularioDatabase[key][mazo];
    }
    return []; // Retorna array vacío si no existe
}

// Función para verificar si existe vocabulario
function existeVocabularioAnime(contenedor, subcontenedor, mazo) {
    const vocabulario = obtenerVocabularioAnime(contenedor, subcontenedor, mazo);
    return vocabulario && vocabulario.length > 0;
}

// Función para obtener todos los mazos de un subcontenedor
function obtenerTodosMazosAnime(contenedor, subcontenedor) {
    const key = `sub${contenedor}_${subcontenedor}`;
    return animesVocabularioDatabase[key] || {};
}

// Función para agregar nuevo vocabulario (útil para expansiones futuras)
function agregarVocabularioAnime(contenedor, subcontenedor, mazo, nuevasPalabras) {
    const key = `sub${contenedor}_${subcontenedor}`;
    
    if (!animesVocabularioDatabase[key]) {
        animesVocabularioDatabase[key] = {};
    }
    
    if (!animesVocabularioDatabase[key][mazo]) {
        animesVocabularioDatabase[key][mazo] = [];
    }
    
    animesVocabularioDatabase[key][mazo] = animesVocabularioDatabase[key][mazo].concat(nuevasPalabras);
    return true;
}
